<h1 class="h2 page-main-title">Technical Analysis</h1>
<p>When you switch on your television around 9.15 a.m. and the stock market bells are just about ringing, you will always find two kinds of analysts telling you about specific stocks.</p>	
						
				<p>One that refers to information about the company such as balance sheet</a>, financial statements, net profit, EPS, PE ratio</a>, and many other factors and one that shows you different charts and tells you how the stock is expected to move. The first analysis is a fundamental analyst while the second is a technical analyst.</p>
<p>Fundamental and technical analysis are two different ways of analyzing stocks and they are very different in their methodologies.</p>
<p>It requires the use of complex strategies, movement indicators, chart patterns, and more. Technical analysts use many such tools at their disposal to predict future movements in price that helps traders book profits in a short period of time. However, there’s much more to the technical analysis of stocks.</p>
<div id="toc_container" class="no_bullets"><p class="toc_title">In this article</p><ul class="toc_list"><li><a href="#What_is_Technical_Analysis">What is Technical Analysis</a></li><li><a href="#Types_of_Charts">Types of Charts</a></li><li><a href="#How_do_they_differ">How do they differ?</a></li><li><a href="#Support_and_Resistance">Support and Resistance:</a></li></ul></div>
<h2><span id="What_is_Technical_Analysis"><strong>What is Technical Analysis</strong></span></h2>
<p>Technical Analysis is at the other end of the stock analysis spectrum. It uses charts instead of annual reports</a> and charts and patterns instead of arriving at an intrinsic value</a>. Stock market technical analysis does use the market price of the stock to predict future patterns and analyze historical ones but does not concern itself with analyzing factors affecting the market price. It studies trends in price, volumes, and moving averages over a period of time.</p>
<p>Trends in the volume show how long such a trend in price will prevail. So if there is a downtrend in volume, this means that trends might not exist for a long time. Like price and volume, there are more indicators, charts of which are analyzed by technical analysts.</p>
<p>Technical analysts, after finding out if there is an uptrend or a downtrend in the metric, find out how long the trend has been there and if there is a visible pattern historically to see if such a pattern may arise in the future. This analysis focuses on quick buying and selling and hence aids stocks traders more.</p>
<p>Therefore three main strongholds of share market technical analysis are:</p><div class="groww-content_6" id="groww-1729825157"><div class="stocks-cta-main">
        
</div></div>
<ul>
<li>Price</li>
<li>Volume</li>
<li>Moving Averages</li>
</ul>
<p>Technical analysts study the historical movement in these factors to predict future trends aiding stock traders to make an informed decision.</p>
<p>The two main tools at the disposal of technical analysts are:</p>
<ul>
<li>Charts and graphs</li>
<li>Indicators</li>
<li>Oscillators</li>
<li>Financial metrics</li>
</ul>
<p>Let’s take a look at the different tools that a technical analyst uses individually.</p>
<h2><span id="Types_of_Charts"><strong>Types of Charts</strong></span></h2>
<p>These are the three main popular chart types used in technical analysis:</p>
<p><strong>Line charts: </strong>A line chart is very simple and basic. It consists of a single line that maps the movements in the closing price of the stock. It is widely used to get a basic idea of the trends in stock prices. This chart does not show much and may not give you much material but it gives you a general idea of the price trend.</p>
<p><strong>Bar charts: </strong>Bar charts are a bit more complicated. There are a series of vertical lines. Each line can tell you the opening, closing, high and low price in a single trading session.</p>
<p><strong>Candlestick charts:</strong> Candlestick charts to depict the opening, closing, low and high price of a stock during a trading session but instead of a single vertical line they use candlesticks to depict the same.</p>
<h2><span id="How_do_they_differ"><strong>How do they differ?</strong></span></h2>
<p>Candlesticks plot these prices over a period of time that produces a candlestick-like body. Also, candlesticks take more space whereas bar charts are more space-optimized and are the favorites of many analysts.</p>
<p>While there are other types of technical analysis charts, these are three primary ones used by analysts on a day to day basis.</p>
<p><strong>Oscillators, moving averages, financial metrics and more</strong></p>
<p>Technical analysis does not suffice itself with these charts. There are many other metrics that analysts keep in mind before advising a buy/sell decision for a particular stock.</p>
<p>Here are a few things analysts consider:</p>
<ol>
<li style="list-style-type: none;">
<ol>
<li><strong>Momentum oscillators: These </strong>are measures in the range of 0 to 100. Momentum oscillators are used to determine the movement and the intensity of a stock.</li>
<li><strong>Relative Strength Index: </strong>RSI is a type of momentum oscillator that also ranges between 0 to 100. RSI will tell you if a stock is overbought or oversold. RSI 70 tells a stock is overbought and under 30 means a stock is oversold.</li>
<li><strong>Daily moving average</strong>: The daily closing price of a stock is plotted on a chart and then a line used to plot the daily moving average of all those prices. You can find 150-DMA, 200-DMA and more for almost every stock.</li>
</ol>
</li>
</ol>
<ul>
<li><strong>Bollinger bands: </strong>Bollinger bands are an extension to the DMA. Bollinger bands are a combination of three lines: the DMA, an upper limit line and a lower limit line. The upper limit line is a line drawn above DMA and the lower limit is a line drawn below it. These lines are used to find out how much and in which direction has the stock moved away from its moving average.</li>
</ul>
<h2><span id="Support_and_Resistance"><strong>Support and Resistance</strong>:</span></h2>
<p>These are two terms that you will hear a lot from technical analysts. These terms are used to indicate the price levels. A support level indicates a stock’s low level whereas resistance indicates the stock’s price is on an upwards trend. Support prompts a trader to buy a stock whereas resistance is a signal to sell.</p>
<p><strong>Technical or Fundamental: which one should you pick? </strong></p>
<p>Using any one of the methodologies on a standalone basis is harmful to a well-informed investment decision. Experts suggest investing in using technical analysis and fundamental analysis.</p>
<p>You should choose a stock that has strong fundamentals and let the technical analysis also aid your buying decision. Most investors use both methods. It is true that technical analysis weighs heavier when it comes to short trading and fundamental analysis more in terms of long term wealth creation, but anyone of the analysis methods should not be left alone. It is better to look at them in unison.</p>

				
