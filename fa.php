<h2><span id="Fundamental_Analysis"><strong>Fundamental Analysis</strong></span></h2>
<p>The primary distinction between fundamental and technical analysis is that the former involves the process of understanding a stock’s intrinsic or inherent value through the analysis of various factors. In this regard, analysts study elements that can make an impact on the security’s integral value.</p>
<ul>
<li>
<h3><span id="Process"><strong>Process</strong></span></h3>
</li>
</ul>
<p>Fundamental analysis is an extensive process wherein every factor that has an influence over the price of a security is minutely investigated to arrive at a conclusion. In doing so it helps analysts to understand if a stock is priced correctly with respect to the broader market.</p>
<p>Fundamental analysis takes into account both macro and microeconomic factors that can influence the price of stocks to facilitate a comprehensive analysis. For instance, to derive a conclusive result, analysts study broader components like the country’s economic and industrial conditions, as well as, more particular elements like a company’s management to determine the price of its stocks.</p>
<p>Furthermore, the method also makes use of several qualitative and quantitative metrics to determine the well-being of the company in question. This is another key difference between fundamental and technical analysis.</p>
<p>Some of the qualitative indicators that analysts popularly use to reach the intrinsic value of a stock</a> are –</p>
<ol>
<li>Management competency</li>
<li>Intangible assets</li>
<li>Competitive edge</li>
<li>Business model</li>
<li>Corporate governance</li>
<li>Industry-wide growth</li>
<li>Customer base</li>
<li>Business cycles</li>
</ol>
<p>A few of the quantitative indicators are –</p><div class="groww-content_6" id="groww-1126102672"><div class="stocks-cta-main">
        
</div></div>
<ol>
<li>Company’s annual revenue</li>
<li>Total profit</li>
<li>Tangible assets</li>
<li>Accounting ratios</li>
</ol>
<p>Apart from these, a fundamental analyst must also be aware of the socio-economic and political factors that might influence a stock’s value in the future.</p>
<ul>
<li>
<h3><span id="Inference"><strong>Inference</strong></span></h3>
</li>
</ul>
<p>Now, if according to fundamental analysis, the intrinsic value of a stock is less than its market price then that security is undervalued. Conversely, if per a fundamental analysis the intrinsic value of a stock is more than its market price then that security is overvalued.</p>
<p>Investors who rely on this determination will then buy a stock if it is undervalued in the expectation to yield significantly higher returns in the long-run. Conversely, market players will assume a short position on a stock when it is overvalued as its prices will soon fall.</p>
<p>In short, fundamental analysis is an approach that is undertaken by investors and works on the principle of “ buy and hold”. Investors who go by fundamental analysis, base their decisions to buy a stock on a comprehensive understanding of a company and hold their investments for a longer period.</p>
<ul>
<li>
<h3><span id="Shortcoming"><strong>Shortcoming</strong></span></h3>
</li>
</ul>
<p>However, misinterpreting any one aspect or overlooking a single fundamental factor in this method of analysis may sometimes lead to an inaccurate representation of intrinsic value.</p>
<h2><span id="Technical_Analysis"><strong>Technical Analysis</strong></span></h2>
<p>While a fundamental analysis of security accounts for an array of factors, technical analysis</a> solely takes historical data directly related to the particular stock into account. That is the primary difference between fundamental analysis and technical analysis.</p>
<ul>
<li>
<h3><span id="Process-2"><strong>Process</strong></span></h3>
</li>
</ul>
<p>Technical analysts base their calculations on data that broadly involves the historical prices of a stock, returns, and volume of trade. Through analysis of said statistics, technical analysts attempt to project future price movements of a security or market. Furthermore, such analysts base their results on the assumption that all other fundamentals have already been factored into the stock’s price, and they remain unchanged.</p>
<p>In short, technical analysis is predominantly based on patterned price movements.</p>
<ul>
<li>
<h3><span id="Inference-2"><strong>Inference</strong></span></h3>
</li>
</ul>
<p>Analysts of this school infer price ranges from a stock’s historical performance patterns which function as a buying and selling signal, also known as support and resistance respectively. That is another point of distinction in the debate of fundamental analysis vs technical analysis.</p>
<p>Now, if the price of a security is moving toward the lower limit of the price-range or support, then a trader shall swoop in to purchase the stock. Per technical analysts, once the price reaches the lower limit, it will shoot up from thereon.</p>
<p>Conversely, if a security’s price is surging towards the upper limit of the price range, then it signals a trader to sell the security in question because once it reaches the ceiling, it will fall from thereon.</p>
<ul>
<li>
<h3><span id="Shortcoming-2"><strong>Shortcoming</strong></span></h3>
</li>
</ul>
<p>However, if the trend does not honour said price-range, then it might continue rising upward or free-falling downward, resulting in substantial losses for investors. Also, technical analysis requires experience as well as knowledge of advanced concepts of stock markets</a>. It is more suited for traders who are looking for short term gains, rather than novice investors or individuals who want to invest in a security based on its long term wealth creation. Investing in stock markets based on technical analysis alone is riskier, therefore.</p>
<h2><span id="When_to_Use_Fundamental_Analysis_and_Technical_Analysis"><strong>When to Use Fundamental Analysis and Technical Analysis?</strong></span></h2>
<p>Another critical point of fundamental analysis vs technical analysis is their usage by market participants. Typically, the securities market comprises two types of investors – long-term investors and short-term traders.</p>
<ul>
<li><strong>Long-term investment</strong></li>
</ul>
<p>Long-term investment by virtue requires a more profound analytical approach compared to short-term trading. Therefore, the former category of investors typically resorts to fundamental analysis. That way, investors educate themselves which stock would yield maximum returns in the long-run based on their intrinsic values.</p>
<p>Investors can also use technical analysis for long-term investment by coupling it with its fundamental counterpart to reach a more concrete conclusion.</p>
<ul>
<li><strong>Short-term trading</strong></li>
</ul>
<p>Investors predominantly use technical analysis to determine short-term earning potential of security. Since future projections reached via technical analysis is based on short-term historical data, it cannot be a basis for long-term decisions.</p>
<p>However, unusual events pertinent to factors that determine a stock’s true value might also cause a dramatic price change in the short-run. Therefore, investors might benefit from fundamental analysis in short-term trading; nonetheless, such cases are rare.</p>
