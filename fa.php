<p>The following table illustrates the key points of difference between fundamental analysis and technical analysis.</p>
<table style="font-size:20px;"border="1" cellpadding="5" cellspacing="1" class="ten">
<tbody>
<tr>
<td><strong>Parameters</strong></td>
<td><strong>Technical analysis</strong></td>
<td><strong>Fundamental analysis</strong></td>
</tr>
<tr>
<td><strong>Purpose </strong></td>
<td>Determining security or market’s prospective patterns by analysing historical price movements and volumes of trade.</td>
<td>Understanding a security’s fundamentals via its financial performance, management, and overall market conditions.</td>
</tr>
<tr>
<td><strong>Usage</strong></td>
<td>Investors typically use it for short-term trading.</td>
<td>Market participants predominantly utilise it for long-term investment.</td>
</tr>
<tr>
<td><strong>Buy and sell signal</strong></td>
<td>Support and resistance, moving averages, trend lines, and momentum-based indicators.</td>
<td>Overvalued and undervalued stocks.</td>
</tr>
