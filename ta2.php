<h2><span id="2Simple_Moving_Average_SMA">2.Simple Moving Average (SMA)</span></h2>
<p>Moving averages are one of the main indicators in technical analysis, and is available for different time periods i.e 20 days, 50 days and 200 days Simple Moving Average (SMA).</p>
<p>In this, the daily average is taken for a given number of days. This tool is used by analysts to make buying or selling decisions.</p>
<p>The tool is commonly used to smooth price data and technical indicators. The longer the period of the moving average, the better the results, but more lag is introduced between the Simple Moving Average and the source.</p>
<p>Price crossing Simple Moving Average is often used to activate trading signals. When prices cross above the Simple Moving Average, an analyst might want to go long or cover short and when they cross below the moving average, he might want to go short or exit long.</p>
<p>SMAs are often used to get trend direction. If the SMA is moving up, the trend is up. If the SMA is moving down, the trend is down.</p>
<p>&nbsp;</p>
<h2><span id="3_Moving_Average_Convergence_and_Divergence_MACD">3. Moving Average Convergence and Divergence (MACD)</span></h2>
<p>This tool is used to spot moving averages that are showing a new trend, whether it is bullish or bearish. Though this tool is somewhat the same as Simple Moving Average, it is different in terms of calculations.</p>
<p>This tool is not used to find out overbought and oversold conditions. The Moving Average Convergence and Divergence is plotted on a chart with zero as the equilibrium. The amount of divergence between the main line and trigger line is also plotted on this chart.</p>
<p>During a period when there is a strong trend, the two MACD lines will diverge. During sideways consolidation, they will come closer and is called convergence. This might often lead to crisscrossing one another few times.</p>
<p>MACD crossing above zero is considered bullish while crossing below zero is bearish. Secondly, when MACD turns up from below zero it is considered bullish. When it turns down from above zero it is considered bearish.</p>
<h2><span id="4_Relative_Strength_Index_RSI">4. Relative Strength Index (RSI)</span></h2>
<p>The relative strength index is a momentum indicator that measures the magnitude of latest price changes to assess over purchased or oversold conditions in the price of a share or other asset.</p>
<p>The index ranges from 0 to 100 and is considered over purchased when above 70 and oversold when below 30. These traditional levels can also be adjusted if needed to better fit the security.</p>
<p>This index also often forms chart patterns that may not show on the underlying price chart, such as double tops and bottoms and trend lines. Also, while looking at the chart, one should look for support or resistance on the RSI.</p>
<p>The divergence between the Relative Strength Index and price which is often a helpful reversal indicator. The Relative Strength Index needs a certain amount of lead up time in order to operate successfully. It helps in detecting movements which might not be as readily noticeable on the bar chart.</p>
<p>The most commonly used Relative Strength Index is the 14 day RSI but other duration RSIs can also be used.</p>
<p>Also, it shows when it was last in the overbought or oversold region and the movement can be used along with other indicators like Bollinger Bands and Candle Stick to determine price movements.</p>
<p>&nbsp;</p>
<h2><span id="5_Parabolic_SAR">5. Parabolic SAR</span></h2>
<p>This tool is a bit complex and has been designed for experts. The parabolic SAR is an indicator used to decide the price direction of an asset, as well as draw attention to when the price direction is changing.</p>
<p>Apart from giving current price directions, this tool also gives possible entry and exit signals.</p>
<p>The Parabolic SAR indicator forms a parabola made of small dots that are either above or below the trading price. When the parabola is below the share price, it acts as a support and trail stop area, while showing bullish up-trending price action.</p>
<p>When the share price falls below a single dot, then it is a stop loss and the investor should limit his loss by selling the stock.</p>
<p>The parabola will then start to trend down as it trades above the stock price, showing a resistance level followed by bearish price action. Traders can use the upper parabola break as a buy-cover. The Parabolic SAR can be used along with other indicators like stochastics, candlesticks and moving averages to spot accurate entries and exits.</p>
