<h2><span id="What_is_a_Demat_Account"><strong>What is a Demat Account?</strong></span></h2>
<p>Demat, or dematerialised account, is a form of an online portfolio that holds a customer’s shares and other securities. It has negated the necessity of holding and trading physical share certificates. Demat trading was first introduced in India in 1996, for NSE</a> transactions. As per SEBI regulations, all shares and debentures of listed companies have to be dematerialised in order to carry out transactions in any stock exchange</a> from 31st March 2019.</p>
<p><strong>Demat account </strong>is used to hold shares and securities in an electronic (dematerialised) format. These accounts can also be used to create a portfolio of one’s bonds, ETFs</a>, mutual funds</a>, and such similar stock market assets.</p>
<h2><span id="Types_of_Demat_Account"><strong>Types of Demat Account</strong></span></h2>
<p>An investor can opt to <strong>open demat account</strong> of any of the following types :-</p>
<p><strong>Regular Demat account – </strong>All residing Indian citizens are eligible to open regular Demat accounts.</p>
<ul>
<li><strong>Repatriable Demat account – </strong>Non-resident Indians can open Demat accounts of repatriable types. One can transfer money from overseas through such accounts provided it is linked to an NRE bank account.</li>
</ul>
<ul>
<li><strong>Non-repatriable Demat account – </strong>Non-repatriable accounts are also for NRIs, however, these accounts cannot be used to transfer funds from abroad. An individual has to link an NRO bank account to own and operate this type of <strong>Demat account</strong>.</li>
</ul>
<p>Customers holding Demat accounts need to open a trading account, to buy or sell securities from the stock market. While respective Depositories and Depository Participants regulate Demat accounts, a trading account follows the regulations mandated by SEBI</a>.</p>
<h2><span id="Depository_and_Depository_Participants"><strong>Depository and Depository Participants</strong></span></h2>
<p>There are primarily two different entities involved in Demat accounts, Depository, and Depository Participants. Depositories are responsible for managing the financial investment portfolio of investors, and Depository Participants (DPs) act as agents between investors and depositors. National Securities Depository Limited (NSDL) and Central Depository Services Limited (CDSL) are two Depositories operating in India, all numerous commercial banks and brokerage agencies are depository participants.</p>
<p>DPs are responsible for handling individual customers and processing their request, as well as overseeing transactions and suggesting potential; stock market</a> investments to their customers.</p>
<h2><span id="Advantages_of_Demat_Accounts"><strong>Advantages of Demat Accounts</strong></span></h2>
<p>Investors who opt to <strong>open Demat account </strong>can enjoy several benefits. Here are some of the most common benefits.</p><div class="groww-content_6" id="groww-1576307770"><div class="stocks-cta-main">
        
</div></div>
<ul>
<li>Demat accounts eliminate the risk of damage, forgery, misplacement, or theft of physical shares.</li>
<li>The electronic system is also considerably simpler and can be completed within hours. It has eliminated several time-consuming operations, which has made the entire process streamlined and time-saving.</li>
<li>Demat accounts come with remote access benefit, provided individuals have a registered for net banking facility with the concerned financial institution.</li>
<li>Investors can merge bank accounts with dematerialised accounts to facilitate electronic fund transfer.</li>
<li>Customers can benefit from a nomination facility if they <strong>open Demat account online</strong>.</li>
<li>Account-holders with a specific unit of securities in their portfolio can opt to freeze their accounts for a specific period. This can prove helpful to avoid any unwanted transaction into one’s Demat account.</li>
</ul>
<h2><span id="How_to_Open_a_Demat_Account"><strong>How to Open a Demat Account?</strong></span></h2>
<p>Dematerialised accounts can be opened by any investor by simply registering with their preferred broker. Investors will have to fill up an application form, and submit the same along with some essential documents (regarding proof of income, proof of identity, and proof of address).</p>
<p>After submission of the same, in-person verification process is initiated. Once that is successfully completed, investors receive their unique client ID and account number. This can be used to link with a trading account and purchase or sell shares and stocks, and access the online personal database.</p>
<p>Investors who have completed the process to <strong>open Demat account online </strong>receive a letter from the respective depository, containing all the details of their Demat account number. Account number issued by CDSL carries a 16-digit numerical value, whereas NSDL issues a 16 digit numerical code preceded by ‘IN’.</p>
<h2><span id="Demat_Account_Number_and_DP_ID"><strong>Demat Account Number and DP ID</strong></span></h2>
<p>Investors are also issued a DP ID, or Depository Participant ID, by their preferred broking firm or other financial institutions. DP ID forms a part of one’s account number, as the first eight-digit of the account number is denoted by this ID. Both depository and depository participants use this data when an investor converts physical shares to Demat, transfers shares from one Demat account to another, or transfer money from Demat account to bank account.</p>
<h3><span id="Conversion_of_Shares"><strong>Conversion of Shares</strong></span></h3>
<p>Currently, every share or security certificate issued after 31st march 2019 is held on dematerialised accounts, to comply with the latest SEBI rules. Investors can also transfer shares from one Demat account to another; this process is usually available</p>
<p>While shifting from one broker to another. Investors can also opt for transfer of shares if they wish to merge several portfolios into one account.</p>
<p>As this process does not initiate any handover of ownership, a trading</a> account does not play a role in such transfers. Investors are not liable for any tax implications for the same either.</p>
<h3><span id="Dematerialisation_or_Rematerialisation"><strong>Dematerialisation or Rematerialisation</strong></span></h3>
<p>Dematerialisation indicates the process of digitising physical shares and debenture certificates under one of the two depositories in India. Rematerialisation indicates the process of changing the electronic certificates into physical ones. Investors opt for rematerialisation to avoid maintenance charge for Demat accounts that holds a minimum number of shares.</p>
<p>Investors can approach the depository participant with a Remat Request Form in order to convert their securities into physical certificates. In this case, every unit will be issued a unique identification number by the registrar and transfer agents (RTA), facilitating the conversion process.</p>
<h3><span id="Demat_Account_Charges"><strong>Demat Account Charges</strong></span></h3>
<p>Although any investor can open a <strong>free Demat account</strong>, there are certain charges that are levied on that account to ensure its smooth operations. Each brokerage firm (including banks) come with their unique brokerage charges. Here are some of those –</p>
<ul>
<li><strong>Annual maintenance charges – </strong>Almost every firm levies a fee as an annual maintenance charge for Demat account. Depositories follow specific guidelines to calculate the fee applicable for each investor.</li>
</ul>
<p>SEBI has implanted a revised rate for Basic Services Demat Account, or BSDA, from 1st June 2019. According to the revised guidelines, no annual maintenance charge will be applicable for debt securities of up to Rs.1 lakh, while a maximum of Rs.100 can be levied on holdings of Rs.1 lakh to Rs.2 lakh.</p>
<ul>
<li><strong>Custodian fees – </strong>Depository partners charge a custodian fee as a one-time or annual basis. The sum is paid directly to the depository (NDSL or CDSL) by the company.</li>
</ul>
<ul>
<li><strong>Demat and Remat charges – </strong>Such expenses are levied as a percentage of the total value of shares purchased or sold, to cover all digitisation or physical print costs of securities.</li>
</ul>
<p>Other than the above mentioned fees, an investor is also liable to pay fees like credit charges, applicable taxes and CESS, rejected instruction charges, etc.</p>
<p>Demat accounts play a crucial role in stock market investments, as it is one of the most common methods of investing in the stock market. However, recently, several online platforms provide the benefit of online trading without such accounts.</p>
<p><strong>FAQ</strong></p>
<ul>
<li><strong>Who can open a Demat account?</strong></li>
</ul>
<p><strong>Ans – </strong>Any residential or non-resident Indian can open a Demat account. Demat accounts can also be issued in the name of minors, provided it is monitored by their legal guardian.</p>
<ul>
<li><strong>Can Demat accounts be opened jointly?</strong></li>
</ul>
<p><strong>Ans – </strong>Yes, it is possible to open a joint Demat account. A maximum of 3 account holders is available for a single account, including the main account holder and two joint account holders.</p>
<ul>
<li><strong>Is a Demat account compulsory to apply for an IPO?</strong></li>
</ul>
<p><strong>Ans – </strong>No, investors can apply and obtain shares of IPO in physical form. However, it is better to apply for an IPO in dematerialised form as the shares issued are only tradable online.</p>
<p><strong>Is a Demat account compulsory for availing a systematic investment plan?</strong></p>
<p><strong>Ans – </strong>No, Demat accounts are primarily for the online transaction of shares on a stock exchange. Any investment in mutual funds, including SIPs, can be carried out directly by the investor, or their preferred financial institution.</p>

