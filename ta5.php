 <h1>
            Simple Moving Average (SMA)
        </h1>



<p>Moving averages are one of the core indicators in technical analysis, and there are a variety of different versions. SMA is the easiest moving average to construct. It is simply the average price over the specified <em>period</em>. The average is called "moving" because it is plotted on the chart bar by bar, forming a line that moves along the chart as the average value changes.</p>
<p>&nbsp;</p>
<center>
<aside class="image-block-large">
                                                
                                <img src="images/sma1.png"  
alt="Image: SMA 3" 
title="Image: SMA 3" />
                                </aside></center>
<p>&nbsp;</p>

<h3>
            How this indicator works
        </h3>
        
        
                  
        
						<ul>
							<li>SMAs are often used to determine trend direction. If the SMA is moving up, the trend is up. If the SMA is moving down, the trend is down. A 200-bar SMA is common proxy for the long term trend. 50-bar SMAs are typically used to gauge the intermediate trend. Shorter period SMAs can be used to determine shorter term trends.</li>
							<li>SMAs are commonly used to smooth price data and technical indicators. The longer the period of the SMA, the smoother the result, but the more lag that is introduced between the SMA and the source.</li>
							<li>Price crossing SMA is often used to trigger trading signals. When prices cross above the SMA, you might want to go long or cover short; when they cross below the SMA, you might want to go short or exit long.</li>
						</ul>
						<p>&nbsp;</p>
						<center>
<aside class="image-block-large">
                                                
                                <img src="images/sma2.png"  
alt="Image: SMA 3" 
title="Image: SMA 3" />
                                </aside></center><p>&nbsp;</p>
						
						
						<ul>
							<li>SMA Crossing SMA is another common trading signal. When a short period SMA crosses above a long period SMA, you may want to go long. You may want to go short when the short-term SMA crosses back below the long-term SMA.</li>
						</ul>
						<p>&nbsp;</p>
						<center>
<aside class="image-block-large">
                                                
                                <img src="images/sma3.png"  
alt="Image: SMA 3" 
