<h2><span id="Participants_of_the_Stock_Market"><strong>Participants of the Stock Market </strong></span></h2>
<p>The stock market is an avenue where investors trade in shares, bonds, and derivatives. This trading is facilitated by stock exchanges, which can be thought of as markets that connect buyers and sellers. Four participants are involved in the trading of shares in the Indian stock market.</p>
<p><strong>1. Securities and Exchange Board of India</a> (SEBI): </strong>SEBI is the regulator of stock markets in India and ensures that securities markets in India work in order. SEBI lays down regulatory frameworks were exchanges, companies, brokerages, and other participants have to abide by to protect investors&#8217; interests.</p><div class="groww-content_8" id="groww-296901068"><div class="row blog_cta">
	
</div></div>
<p><strong>2.Stock exchanges</a>: </strong>The stock market is an avenue where investors trade in shares, bonds, and derivatives. This trading is facilitated by stock exchanges.In India, there are two primary stock exchanges on which companies are listed.</p>
<ul>
<li>Bombay Stock Exchange</a> (BSE) &#8211; Sensex is its index</li>
<li>National Stock Exchange</a> (NSE) &#8211; Nifty is its Index</li>
</ul>
<p><strong>3. Stock brokers</a>/brokerages:</strong> A broker is an intermediary ( person or a firm) that executes buy and sell orders for investors in return of a fee or a commission.</p>
<p><strong>4. Investors and traders:</strong> Stocks are units of a company&#8217;s market value. Investors are individuals who purchase stocks to become part owners in the company. Trading involves buying or selling this equity. To understand how to share market works, the next thing is to learn about primary and secondary markets</p>
<h3><span id="1_Primary_Markets"><strong>1. Primary Markets </strong></span></h3>
<p>The primary stock market</a> provides an opportunity to issuers of stocks, especially corporates, to raise resources to meet their investment requirements and discharge some obligations and liabilities.</p>
<p>A company lists its shares in the primary market through an Initial Public Offering or IPO</a>. Through an IPO, a company sells its shares for the first time to the public. An IPO opens for a particular period. Within this window, investors can bid for the shares and buy them at the issue price announced by the company.</p>
<p>Once the subscription period is over, the shares are allotted to the bidders. The companies are then called public  because they have given out their shares to the common public.</p>
<p>For this, companies need to pay a fee to the stock exchanges. They are also required to provide all important details of the company&#8217;s financial information such as quarterly/annual reports, balance sheets, income statements, along with information on new projects or future objectives, etc. to the stock markets.</p>
<h3><span id="2_Secondary_Market"><strong>2. Secondary Market</strong></span></h3>
<p>The last step involves listing the company on the stock market, which means that the stock issued during the IPO can now freely be bought and sold. The secondary stock market</a> is where shares of a company are traded after being initially offered to the public in the primary market. It is a market where buyers and sellers meet directly.</p>

<h2><span id="Trading_in_the_Stock_Market"><strong>Trading in the Stock Market</strong></span></h2>
<p>Once listed on the stock exchanges, the stocks issued by companies can be traded in the secondary market to make profits or cut losses. This buying and selling of stocks listed on the exchanges are done by stockbrokers /brokerage firms, that act as the middleman between investors and the stock exchange.</p>
<p>Your broker passes on your buy order for shares to the stock exchange. The stock exchange searches for a sell order for the same share.</p>
<p>Once a seller and a buyer are found and fixed, a price is agreed to finalize the transaction. Post that the stock exchange communicates to your broker that your order has been confirmed.</p>
<p>This message is then passed on to you by the broker.</p>

<p>Meanwhile, the stock exchange also confirms the details of the buyers and the sellers of shares to ensure the parties donâ€™t default.</p>
<p>It then facilitates the actual transfer of ownership of shares from sellers to buyers. This process is called the settlement cycle.</p>
<p>Earlier, it used to take weeks to settle stock trades. But now, this has been brought down to T+2 days.</p>
<p>For example,</p>
<p>If you trade a stock today, you will get your shares deposited in your Demat/trading account by the day after tomorrow (i.e. within two working days).</p>
<p>The stock exchange also ensures that the trade of stocks is honored during the settlement.</p>
<p>If the settlement cycle doesnâ€™t happen in T+2 days, the sanctity of the stock market is lost, because it means trades may not be upheld.</p>
<p>Stockbrokers</a> identify their clients by a unique code assigned to an investor.</p>
<p>After the transaction is done by an investor, the stockbroker issues him/her a contract note which provides details of the transaction such as time and date of the stock trade.</p>
<p>Apart from the purchase price of a stock, an investor is also supposed to pay brokerage fees, stamp duty, and securities transaction tax.</p>
<p>In case of a sale transaction, these costs are reduced from the sale proceeds, and then the remaining amount is paid to the investor.</p>
<p>At the broker and stock exchange levels, there are multiple entities/parties involved in the communication chain like brokerage order department, exchange floor traders, etc.</p>
<p>But the stock trading process has become electronic today. So, the process of matching buyers and sellers is done online and as a result, trading happens within minutes.</p>
<h2><span id="Pricing_of_Shares_in_the_Stock_Market"><strong>Pricing of Shares in the Stock Market</strong></span></h2>
<p>The key to making money in the stock market is to learn how to properly value a company and its share price in the context of the Indian economy and the firm&#8217;s operating sector.</p>
<p>Let me explain to you how stocks are priced through a simple example.</p>
<p>Letâ€™s say you bought a notebook for â‚¹100. The next day, a friend of yours offered you to sell it for â‚¹150 to him.</p>
<p>So, whatâ€™s the price of the notebook then?</p>
<p>It is from â‚¹150. You can encash â‚¹150 by selling the notebook to him.</p>
<p>But you choose to reject his offer hoping that your other friends may bid more than â‚¹150.</p>
<p>The very next day 3 of your friends offer you â‚¹200, â‚¹250 and â‚¹300 for the notebook respectively.</p>
<p>Now, whatâ€™s the price of the notebook?</p>
<p>It&#8217;s â‚¹300 as this is the highest bid for your notebook. You now know that your possession is valuable and decide to reject the current offers, hoping for a higher bid tomorrow. However, the next day, a fellow student brings a better quality notebook to school with shinier pages.</p>
<p>Your friends are now attracted to this notebook more than yours and this leads to a dip in the value of your notebook. Now only a handful of people are willing to pay for your notebook and that too at the last quoted price i.e â‚¹300.</p>
<p>This is exactly how demand and supply affect the price of a share in the stock market.</p>
<p>When the students were optimistic and ready to pay higher cash than its current price, the price appreciated. When a lesser number of students wanted your notebook, the price fell down.</p>
<p>Just keep this small concept in your mind:</p>
<ul>
<li>When the demand for shares is more than supply, price rises.</li>
<li>When the demand for shares is less than supply, price falls.</li>
</ul>
<p>The Indian stock exchanges, BSE and NSE, have algorithms that determine the price of stocks on the basis of volume traded and these prices change pretty fast. So this is how the stock market works in India. There is definitely more to it however this is a good starting point to develop further understanding.</p>
