<h1 class="h2">How Does the Stock Market Work in India<span class="wtr-time-wrap after-title"><span class="wtr-time-number"></h1>
		


	
	
	    	<p>We all know how important it is to invest money in the right avenues to grow wealth. Stock market investment is one such lucrative option that has rewarded steadfast investors with high returns over the years. However, to gain the maximum out of a financial instrument, it is essential to know about its workings. Letâ€™s get back to the basics and learn how the stock market works in India Read on!</p>
