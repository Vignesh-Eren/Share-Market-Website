<h3 class="heading">Inverse hammer</h3>
<p>A similarly bullish pattern is the inverted hammer. The only difference being that the upper wick is long, while the lower wick is short.</p>
<p>It indicates a buying pressure, followed by a selling pressure that was not strong enough to drive the market price down. The inverse hammer suggests that buyers will soon have control of the market.</p>
