<h2 class="heading">What is a candlestick?</h2>
<p>A candlestick is a way of displaying information about an asset’s price movement. Candlestick charts are one of the most popular components of technical analysis, enabling traders to interpret price information quickly and from just a few price bars.</p>
<p>This article focuses on a daily chart, wherein each candlestick details a single day’s trading. It has three basic features:</p>
<ul>
<li><strong>The body, </strong>which represents the open-to-close range</li>
<li><strong>The wick</strong>, or shadow, that indicates the intra-day high and low</li>
<li><strong>The colour</strong>, which reveals the direction of market movement – a green (or white) body indicates a price increase, while a red (or black) body shows a price decrease</li>
</ul>
<p>Over time, individual candlesticks form patterns that traders can use to recognise major support and resistance levels. There are a great many candlestick patterns that indicate an opportunity within a market – some provide insight into the balance between buying and selling pressures, while others identify continuation patterns or market indecision.</p>
<p>Before you start trading, it’s important to familiarise yourself with the basics of candlestick patterns and how they can inform your decisions.</p>
<h2 class="heading">Practise reading candlestick patterns</h2>
<p>The best way to learn to read candlestick patterns is to practise entering and exiting trades from the signals they give. You can develop your skills in a risk-free environment by opening an IG demo account</a>, or if you feel confident enough to start trading, you can open a live account</a> today.</p>
<p>When using any candlestick pattern, it is important to remember that although they are great for quickly predicting trends, they should be used alongside other forms of technical analysis</a> to confirm the overall trend. You can learn more about candlesticks and technical analysis with IG Academy</a>’s online courses.</p>
<h2 class="heading">Six bullish candlestick patterns</h2>
<p>Bullish patterns may form after a market downtrend, and signal a reversal of price movement. They are an indicator for traders to consider opening a long position to profit from any upward trajectory.</p>
<h3 class="heading">Hammer</h3>
<p>The hammer candlestick pattern is formed of a short body with a long lower wick, and is found at the bottom of a downward trend.</p>
<p>A hammer shows that although there were selling pressures during the day, ultimately a strong buying pressure drove the price back up. The colour of the body can vary, but green hammers indicate a stronger bull market than red hammers.</p>

