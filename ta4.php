<h3 class="heading">Evening star</h3>
<p>The evening star is a three-candlestick pattern that is the equivalent of the bullish morning star. It is formed of a short candle sandwiched between a long green candle and a large red candlestick.</p>
<p>It indicates the reversal of an uptrend, and is particularly strong when the third candlestick erases the gains of the first candle.</p>
