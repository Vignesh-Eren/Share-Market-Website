<h3 class="heading">Rising three methods</h3>
<p>The opposite is true for the bullish pattern, called the ‘rising three methods’ candlestick pattern. It comprises of three short reds sandwiched within the range of two long greens. The pattern shows traders that, despite some selling pressure, buyers are retaining control of the market.</p>
