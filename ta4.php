<h3 class="heading">Spinning top</h3>
<p>The spinning top candlestick pattern has a short body centred between wicks of equal length. The pattern indicates indecision in the market, resulting in no meaningful change in price: the bulls sent the price higher, while the bears pushed it low again. Spinning tops are often interpreted as a period of consolidation, or rest, following a significant uptrend or downtrend.</p>
<p>On its own the spinning top is a relatively benign signal, but they can be interpreted as a sign of things to come as it signifies that the current market pressure is losing control.</p>
