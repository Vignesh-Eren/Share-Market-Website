<h3 class="heading">Piercing line</h3>
<p>The piercing line is also a two-stick pattern, made up of a long red candle, followed by a long green candle.</p>
<p>There is usually a significant gap down between the first candlestick’s closing price, and the green candlestick’s opening. It indicates a strong buying pressure, as the price is pushed up to or above the mid-price of the previous day.</p>
