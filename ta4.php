<h3 class="heading">Three white soldiers</h3>
<p>The three white soldiers pattern occurs over three days. It consists of consecutive long green (or white) candles with small wicks, which open and close progressively higher than the previous day.</p>
<p>It is a very strong bullish signal that occurs after a downtrend, and shows a steady advance of buying pressure.</p>
