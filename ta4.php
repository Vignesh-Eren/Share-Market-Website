<h2 class="heading">Six bearish candlestick patterns</h2>
<p>Bearish candlestick patterns usually form after an uptrend, and signal a point of resistance. Heavy pessimism about the market price often causes traders to close their long positions, and open a short position to take advantage of the falling price.</p>
<h3 class="heading">Hanging man</h3>
<p>The hanging man is the bearish equivalent of a hammer; it has the same shape but forms at the end of an uptrend.</p>
<p>It indicates that there was a significant sell-off during the day, but that buyers were able to push the price up again. The large sell-off is often seen as an indication that the bulls are losing control of the market.</p>
