<h3 class="heading">Three black crows</h3>
<p>The three black crows candlestick pattern comprises of three consecutive long red candles with short or non-existent wicks. Each session opens at a similar price to the previous day, but selling pressures push the price lower and lower with each close.</p>
<p>Traders interpret this pattern as the start of a bearish downtrend, as the sellers have overtaken the buyers during three successive trading days.</p>
