<h3 class="heading">Falling three methods</h3>
<p>Three-method formation patterns are used to predict the continuation of a current trend, be it bearish or bullish.</p>
<p>The bearish pattern is called the ‘falling three methods’. It is formed of a long red body, followed by three small green bodies, and another red body – the green candles are all contained within the range of the bearish bodies. It shows traders that the bulls do not have enough strength to reverse the trend.</p>
