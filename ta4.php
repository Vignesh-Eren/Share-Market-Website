<h2 class="heading">Four continuation candlestick patterns</h2>
<p>If a candlestick pattern doesn’t indicate a change in market direction, it is what is known as a continuation pattern. These can help traders to identify a period of rest in the market, when there is market indecision or neutral price movement.</p>
<h3 class="heading">Doji</h3>
<p>When a market’s open and close are almost at the same price point, the candlestick resembles a cross or plus sign – traders should look out for a short to non-existent body, with wicks of varying length.</p>
<p>This doji’s pattern conveys a struggle between buyers and sellers that results in no net gain for either side. Alone a doji is neutral signal, but it can be found in reversal patterns such as the bullish morning star and bearish evening star.</p>
</div>
