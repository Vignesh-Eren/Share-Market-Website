<h3 class="heading">Morning star</h3>
<p>The morning star candlestick pattern is considered a sign of hope in a bleak market downtrend. It is a three-stick pattern: one short-bodied candle between a long red and a long green. Traditionally, the ‘star’ will have no overlap with the longer bodies, as the market gaps both on open and close.</p>
<p>It signals that the selling pressure of the first day is subsiding, and a bull market is on the horizon.</p>
