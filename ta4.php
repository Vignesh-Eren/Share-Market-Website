<h3 class="heading">Bullish engulfing</h3>
<p>The bullish engulfing pattern is formed of two candlesticks. The first candle is a short red body that is completely engulfed by a larger green candle.</p>
<p>Though the second day opens lower than the first, the bullish market pushes the price up, culminating in an obvious win for buyers.</p>
