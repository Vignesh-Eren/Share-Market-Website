<h3 class="heading">Dark cloud cover</h3>
<p>The dark cloud cover candlestick pattern indicates a bearish reversal – a black cloud over the previous day’s optimism. It comprises two candlesticks: a red candlestick which opens above the previous green body, and closes below its midpoint. </p>
<p>It signals that the bears have taken over the session, pushing the price sharply lower. If the wicks of the candles are short it suggests that the downtrend was extremely decisive.</p>
