<h2><span id="Minimum_amount_needed_to_invest_in_the_share_market_in_India">Minimum amount needed to invest in the share market in India</span></h2>
<p>There is no minimum or maximum amount to invest in Indian stock markets. Since there is no requirement of minimum investment in the share market, it makes it a flexible option.</p>
<p>Investors with any amount of money can start investing in Indian stock markets.</p>
<p>The Indian stock market is represented by 2 stock exchanges</a>, that facilitate the buying and selling of a company’s stock via your stockbroker. These are:</p>
<ol>
<li>National Stock Exchange (NSE), which has around 2000 companies listed on it. The index used here is Nifty.</li>
<li>Bombay Stock Exchange (BSE), which has approximately 5000 companies listed on it. The index used here is Sensex.</li>
</ol>
<p>The company stocks that are traded on these exchanges range from as low as ₹0.5 to as high as ₹70,000.</p>
<p>Also, note that the broker involved in carrying out the transaction will charge you brokerage charges. They will also charge certain taxes mandated by the Government of India.</p>
<p>Thus,</p>
<p>Quantity of share = Capital / (Share price + brokerage fees + taxes)</p>
<p>For example:</p>
<p>Let say there is a company X having a share price of ₹20. The capital you have with you to invest is ₹1000. Then, the number of shares you can purchase is not 50, but 49 – 49.xx, depending on the brokerage fees and taxes.</p>
<h2><span id="Can_NRIs_invest_in_the_stock_markets_Can_Foreigners_Invest_in_the_Indian_Stock_Markets"><strong>Can NRIs invest in the stock markets? Can Foreigners Invest in the Indian Stock Markets?</strong></span></h2>
<p>Indian stock markets are specifically for Indian citizens to trade. But there are ways for foreigners to invest as well.</p>
<p>Portfolio Investment Scheme (PIS), developed by RBI, allows eligible entities, such as foreign institutional investors (FIIs), non-resident Indians (NRIs), persons of Indian origin (PIOs) and qualified foreign investors (QFIs) to invest in stocks and convertible debentures of Indian companies.</p>
<h3><span id="NRIs_and_PIOs_can_invest_in_the_Indian_stock_markets"><strong>NRIs and PIOs can invest in the Indian stock markets</strong></span></h3>
<p>NRIs and PIOs are eligible to trade stocks and convertible debentures of Indian firms through a registered broker.</p>
<p>Who are NRIs and PIOs as per the Finance Bill, 2020?</p>
