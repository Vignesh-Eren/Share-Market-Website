<h1 class="h2">Who can Invest in the Indian Stock Market</h1>
<p>The eligibility to invest in Indian stock markets is very easy to meet.</p>
<p>Indian stock markets have evolved substantially in the last 20 years in terms of risk management, operations, penetration, etc.</p>
<p>However, retail participation is still fairly low. But it is picking up through the equity mutual funds route and by educating the young investors.</p><div class="groww-content_8" id="groww-310032395"><div class="row blog_cta">
	
</div></div>
<p>Indian investors are veering around to the view that only stocks can generate wealth for them in the long term. There are many questions that new investors seek answers for like the minimum amount to invest in the share market, eligibility criteria, types of accounts needed, etc.</p>
<p>Today, we will offer a guide to know who can invest in Indian stock markets.</p>
