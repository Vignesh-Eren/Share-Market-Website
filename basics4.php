<h2><span id="What_is_the_minimum_age_to_invest_in_the_Indian_stock_markets"><strong>What is the minimum age to invest in the Indian stock markets?</strong></span></h2>
<p>As such there is as such no age restriction for investing in stock markets of India.</p>
<p>It’s just that you should be more than 18 years old to create a Demat account and a trading account. To open your Demat and trading account a PAN card is a must. And you can only apply for a PAN card if you are18 years or older.</p>
<h3><span id="Investing_in_the_stock_market_for_minorsunder_18_years_of_age"><strong>Investing in the stock market for minors/under 18 years of age</strong></span></h3>
<p>Even if your age is less than 18 years, it is still possible to open Demat and trading accounts. You can do so by submitting the documents of your guardian.</p>
<p>You can open a Demat and trading account at a brokerage in the name of a minor by the natural guardians (like parents) or the court-appointed guardian.</p>
<p>After verifying all the necessary documents, the depository participant will allow you to trade in Indian stock markets.</p>
<h2><span id="Can_a_student_invest_in_stock_markets_in_India"><strong>Can a student invest in stock markets in India?</strong></span></h2>
<p>Yes. If the student is more than 18 years old, then he will be treated as a regular investor. If he is a minor, then the rules for minors will apply.</p>
<h2><span id="Can_I_invest_in_the_stock_market_in_India_Without_a_Stockbroker"><strong>Can I invest in the stock market in India Without a Stockbroker?</strong></span></h2>
<p>Any person who wishes to invest in Indian stocks, cannot go directly to the stock markets to buy or sell shares. Buying and selling of stocks have to be done through stockbrokers.</p>
<p>A stockbroker is an individual or a financial institute, licensed and authorised by SEBI to trade in stock markets. They also have direct access to the share market. They can act as your agent in share transactions of companies.</p>
<p>A stockbroker can also offer additional services like advice on stocks, debentures, government bonds, and listed property trusts, and non-listed investment options. For the services provided, stockbrokers charge a brokerage fee.</p>
<p>Also, a stockbroker can plan, implement, and monitor your investment portfolio, conduct research, and help you optimize your returns in stock markets.</p>
<h2><span id="How_to_Invest_in_Stocks_Without_a_Demat_account"><strong>How to Invest in Stocks Without a Demat account?</strong></span></h2>
<p>Having a trading</a> and Demat account</a> is mandatory to invest in the Indian stock markets.</p>
<p>The first step is to choose a stockbroker.</p>
<p>Next, open a Demat and a trading account in which the stocks would be electronically linked to your portfolio.</p>
<p>The trading account is similar to your bank account, which needs to be opened with a stockbroker</a>. This account is used for placing orders in the stock markets i.e. to buy or sell stocks.</p>
<p>A Demat account is where stocks are held in a dematerialized form (i.e. electronically instead of physical possession of certificates by investors). It is required to receive or transfer stocks when you buy or sell stocks through your trading account.</p>
<p>Follow these steps to open a trading / Demat account:</p>
<ol>
<li>Approach a stockbroker registered with BSE and NSE</li>
<li>Fill up the KYC form</li>
<li>Attach the necessary documents: identity proof and address proof</li>
<li>Produce the PAN card during the opening of the account</li>
<li>One canceled cheque of the bank account you want to link to your trading account and</li>
<li>Recent passport size photographs of yours</li>
</ol>
<p>So, if you meet all the above criteria, don’t stop yourself from investing in the Indian stock market.</p>
<p>Always remember that there is no perfect time to start investing. Also, there is no minimum investment in the share market in India. An investment decision should not be based on market ups and downs or its speculation. So, avoid investing in a company merely because the stock prices of a particular company are increasing exponentially and make data-backed decisions.</p>
<p>Happy Investing!</p>

