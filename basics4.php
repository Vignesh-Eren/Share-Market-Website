<h2><span id="Qualified_Foreign_Investors_QFIs">Qualified Foreign Investors (QFIs)</span></h2>
<p>At the beginning of the year 2012, the government of India gave a new year gift to the stock markets. It allowed Qualified Foreign Investors (QFIs), including overseas individuals, to invest directly in Indian stock markets.</p>
<p>QFIs shall include individuals, groups, or associations that follow below mention guidelines:</p>
<ol>
<li>Resident in a country that is a member of the Financial Action Task Force (FATF) or a country that is a member of a group which is a member of FATF and</li>
<li>Resident in a country that is a signatory to IOSCO’s MMOU or a signatory of a bilateral MOU with Securities and Exchange Board of India (SEBI).</li>
<li>A QFI should neither be a person resident in India nor should be registered with the SEBI as a Foreign Institutional Investor (FII), sub-account, or Foreign Venture Capital Investor.</li>
<li>Also, QFI should be set up with a SEBI registered Qualified Depository Participant (QDP) to commence activities. The QDP shall provide inter alia custody services.</li>
</ol>
<p>Residents of the following 45 countries shall be eligible to invest as a QFI as per the definition by SEBI guidelines.</p>
