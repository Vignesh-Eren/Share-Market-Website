<h1 class="h2 page-main-title">What is IPO?</h1>
<p>The process of Initial Public Offering (IPO) transforms a privately-held company into a public company. This process also creates an opportunity for smart investors to earn a handsome return on their investments.</p>

<p>Investing in IPOs can be a smart move if you are an informed investor. But not every upcoming IPO is a great opportunity. Benefits and risks go hand-in-hand. Before you join the bandwagon, it is important to understand the basics.</p>
<div id="toc_container" class="no_bullets"><p class="toc_title">In this article</p><ul class="toc_list"><li><a href="#IPO_Meaning">IPO Meaning</a></li><li><a href="#Types_of_IPO">Types of IPO</a></li><li><a href="#How_to_invest_in_an_IPO">How to invest in an IPO?</a></li><li><a href="#Terms_Associated_with_IPO">Terms Associated with IPO</a></li><li><a href="#Eligibility_norms_required_to_invest_in_an_IPO">Eligibility norms required to invest in an IPO</a><ul><li><a href="#On_a_closing_note">On a closing note</a></li></ul></li></ul></div>
<h2><span id="IPO_Meaning">IPO Meaning</span></h2>
<p>Initial Public Offering (IPO) can be defined as the process in which a private company or corporation can become public by selling a portion of its stake to the investors. An IPO is generally initiated to infuse the new equity capital to the firm, to facilitate easy trading of the existing assets, to raise capital for the future or to monetize the investments made by existing stakeholders.</p>

<p>The institutional investors, high net worth individuals (HNIs) and the public can access the details of the first sale of shares in the prospectus. The prospectus is a lengthy document that lists the details of the proposed offerings.</p>
<p>Once the IPO is done, the shares of the firm are listed and can be traded freely in the open market. The stock exchange</a> imposes a minimum free float on the shares both in absolute terms and as a ratio of the total share capital.</p>
<h2><span id="Types_of_IPO">Types of IPO</span></h2>
<p>There are two common types of IPO. They are:</p>
<p><strong>Fixed Price Offering</strong></p>
<p>Fixed Price IPO can be referred to as the issue price that some companies set for the initial sale of their shares. The investors come to know about the price of the stocks</a> that the company decides to make public.</p><div class="groww-content_6" id="groww-180068480"><div class="stocks-cta-main">

</div></div>
<p>The demand for the stocks in the market can be known once the issue is closed. If the investors partake in this IPO, they must ensure that they pay the full price of the shares when making the application.</p>
<p><strong>Book Building Offering</strong></p>
<p>In the case of book building, the company initiating an IPO offers a 20% price band on the stocks to the investors. The interested investors bid on the shares before the final price is decided. Here, the investors need to specify the number of shares they intend to buy and the amount they are willing to pay per share.</p>
<p>The lowest share price is referred to as floor price and the highest stock price is known as cap price. The ultimate decision regarding the price of the shares is determined by investors’ bids.</p>
<h2><span id="How_to_invest_in_an_IPO">How to invest in an IPO?</span></h2>
<p>There are certain steps the investor needs to follow to ensure that they are following the right path to wealth. The steps an investor needs to follow are:</p>
<p><strong>Decision</strong></p>
<p>The primary step for an investor would be to decide the IPO he wants to apply for. Though the existing investors may have the expertise, it could be an intimidating one for the new ones. The investors can form a choice by going through the prospectus of the companies initiating IPO.</p>
<p>The prospectus helps the investors to form an informed idea about the company’s business plan and its purpose for raising stocks in the market. Once the decision has been made, the investor needs to look forward to the next step.</p>
<p><strong>Funding</strong></p>
<p>When an investor has formed the decision regarding the IPO he would like to invest in, the very next step would be to arrange the funds. An investor can use his savings to buy a company’s share.</p>
<p>In case the investor does not have enough savings, he can avail a loan from certain banks and Non-Banking Financial Organisations (NBFCs) at a definite rate of interest.</p>
<p><strong>Opening a Demat-cum-trading account</strong></p>
<p>Any investor without a Demat account cannot apply for an IPO. The function of a Demat account is to provide the investors with the provision to store shares and other financial securities electronically. One can open a Demat account by submitting his Aadhaar card, PAN card, address and identity proofs.</p>
<p><strong>The application process</strong></p>
<p>An investor can apply for an IPO through his bank account or trading account. Some financial organisations will offer you the provision to bunch your Demat, trading and bank accounts.</p>
<p>After an investor has created the demat-cum-trading account, he needs to be familiar with the Application Supported by Blocked Account (ASBA) facility. It is mandatory for every IPO applicant. The ASBA is an application that enables the banks to arrest funds in the applicant’s bank account.</p>
<p>The ASBA application forms are made available to the IPO applicants in both demat and physical form. However, the use of cheques and demand drafts can not be made to avail the facility. An investor needs to specify his demat account number, PAN, bidding details and bank account number in the application.</p>
<p><strong>Bidding</strong></p>
<p>An investor needs to bid while applying for the shares in an IPO. It is done according to the lot size quoted in the company’s prospectus. Lot size can be referred to as the minimum number of shares that an investor has to apply for in an IPO.</p>
<p>A price range is decided and the investors require to bid within the price range. Though an investor can make a revision in his biddings during an IPO, it should be noted that he needs to block the required funds while bidding. In the meantime, the arrested amount in the banks earns interest until the process of allotment is initiated.</p>
<p><strong>Allotment</strong></p>
<p>In many cases, the demand for the shares can exceed the actual number of stocks released in the secondary market. One can also face situations where he can get a fewer number of shares than what he had demanded. In these cases, the banks unlock the arrested funds either entirely or partially.</p>
<p>But, if an investor is lucky enough to get a full allotment, he would receive a CAN (Confirmatory Allotment Note) within six working days after the IPO process is done. After the shares have been allotted, they are credited to the investor’s demat account.</p>
<p>Once the above-mentioned steps are carried on successfully, the investor will have to wait for the listings of the stocks in the share market. It is generally done within seven days after the shares are finalised.</p>
<h2><span id="Terms_Associated_with_IPO">Terms Associated with IPO</span></h2>
<p>To have informed knowledge about IPO, it is necessary that one comes to know about some basic terms used in the process. Some of the commonly used terms are:</p>
<p><strong>Issuer</strong></p>
<p>An issuer can be referred to as the company or the firm who wants to issue shares in the secondary market in order to finance its operations.</p>
<p><strong>Underwriter</strong></p>
<p>An underwriter can be a banker, financial institution, merchant banker or a broker. It assists the company to underwrite their stocks. The underwriters also commit that they will subscribe to the balance shares in case the stocks offered at IPO are not picked by the investors.</p>
<p><strong>Draft Red Herring Prospectus</strong></p>
<p>The DRHP is the document that makes the public know about the company’s IPO listings after the approval made by SEBI. A DRHP contains the following information about the company:</p>
<ul>
<li>Purpose of raising funds through listings</li>
<li>Balance sheet</li>
<li>Promoter’s expenses</li>
<li>Earning statement of the last three years (if applicable)</li>
<li>Net proceeds of the company</li>
<li>Commission and discounts of the underwriter</li>
<li>Details such as the name and address of all the underwriters, officers, directors and stockholder who possess 10% or more than the currently outstanding stock.</li>
<li>Legal opinion on the listings</li>
<li>Copy of the underwriting document</li>
</ul>
<p><strong>Fixed Price IPO and Price Band</strong></p>
<p>Fixed Price IPO can be referred to as the issue price that some companies set for the initial sale of their shares.</p>
<p>A price band can be defined as a value-setting method where a seller offers an upper and lower cost limit, the range within which the interested buyers can place their bids. The range of the price band guides the buyers.</p>
<p><strong>Under subscription and oversubscription</strong></p>
<p>Under subscription takes place when the number of securities applied for is less than the number of shares made available to the public.</p>
<p>Oversubscription is the condition when the number of shares offered to the public is less than the number of shares applied for.</p>
<p><strong>Green Shoe Option</strong></p>
<p>It refers to an overallotment option. It is an underwriting agreement that permits the underwriter to sell more shares than initially planned by the company. It happens in cases when the demand for a share is seen higher than expected. It lets the issuer company release additional shares in the secondary market in the event of oversubscription.</p>
<p><strong>Book Building</strong></p>
<p>The process by which an underwriter or a merchant banker tries to determine the price at which the IPO will be offered is called book building. A book is made by the underwriter where he submits the bids made by the institutional investors and fund managers for the number of shares and the price they are willing to pay.</p>
<p>Once an idea has been made and the price band is decided, the underwriter or the merchant banker decides the IPO price. The issuer company’s shares are open for subscription for three trading days.</p>
<h2><span id="Eligibility_norms_required_to_invest_in_an_IPO">Eligibility norms required to invest in an IPO</span></h2>
<p>Any individual who is an adult and is capable of entering into a legal contract can serve the eligibility norms to apply in the IPO of a company. However, there are some other inevitable norms an investor needs to meet. The eligibility criteria are:</p>
<ul>
<li>It is required that the investor interested in buying a share in an IPO has a PAN card issued by the Income Tax department of the country.</li>
<li>One also needs to have a valid Demat account.</li>
<li>It is not required to have a trading account, a Demat account serves the purpose. However, in case an investor sells the stocks on listings, he will need a trading account.</li>
</ul>
<p>It is often advised to open a trading account along with the Demat account when an investor is looking forward to investing in an IPO for the first time.</p>
<h3><span id="On_a_closing_note">On a closing note</span></h3>
<p>Initial Public Offerings are generally considered beneficial as it lets the issuer company enlarge their equity base and increases the exposure and prestige. At the same time, it provides investors with an opportunity to gain handsome returns. However, one must be watchful of the latest IPOs and have a clear understanding of analysing financial metrics in order to identify the opportunities.</p>



