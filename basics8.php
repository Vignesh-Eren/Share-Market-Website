<h1 class="h2">How to Invest in an IPO Online</h1>

<p><span style="font-weight: 400">Whether you are a stock investor or not, you might have heard the phrase ‘a company went public’ either online or in a newspaper advertisement. What does going public mean? In the simplest terms, ‘going public’ is the process of a private company raising capital by issuing shares for the first time.</span></p>
<p><span style="font-weight: 400">In a private company, the number of shareholders is limited by law. However, it has the option of raising capital from people by making them shareholders in the company and converting into a public limited company. When a private company approaches people to invest in exchange for a shareholding in the company, it launches the first offer called an IPO or Initial Public Offering. Today, we are going to discuss IPOs and also look at how to invest in an IPO.
<div id="toc_container" class="no_bullets"><p class="toc_title">In this article</p><ul class="toc_list"><li><a href="#How_to_apply_for_an_IPO_online">How to apply for an IPO online?</a><ul><li><a href="#How_to_apply_for_an_IPO_online_through_ASBA">How to apply for an IPO online through ASBA</a></li><li><a href="#How_to_apply_for_an_IPO_via_UPI">How to apply for an IPO via UPI</a></li></ul></li><li><a href="#Accounts_Required_to_Apply_for_an_IPO_Online">Accounts Required to Apply for an IPO Online</a></li><li><a href="#Funding_Your_IPO_Application">Funding Your IPO Application</a></li></ul></div>
<h2><span id="How_to_apply_for_an_IPO_online"><b>How to apply for an IPO online?</b></span></h2>
<p><span style="font-weight: 400">You can apply for an IPO online via ASBA facility available with most banks or UPI. Here’s how you can apply for an IPO online through both the facilities:
<h3><span id="How_to_apply_for_an_IPO_online_through_ASBA"><strong>How to apply for an IPO online through ASBA</strong></span></h3>
<p><span style="font-weight: 400">You can apply on your bank&#8217;s website throughout the online ASBA facility. ASBA stands for Application Supported by Blocked Amounts (ASBA). It holds funds in your account without actually debiting it. You can generally find the ASBA facility in e-services or net banking services option. ASBA facility allows you to invest in FPOs and IPOs as it will show all the live issues currently.</span></p>
<p><span style="font-weight: 400">Here we will be using screenshots and examples from HDFC Net Banking ASBA facility. The steps are more or less the same across banking platforms</span></p>
<p><b>Step 1:</b><span style="font-weight: 400"> Log in to your net banking account using your customer ID and password.</span></p>
<p><b>Step 2:</b><span style="font-weight: 400"> Go to the request tab on the left side of the screen.</span></p>
<p><b>Step 3:</b><span style="font-weight: 400"> Scroll down and find the IPO/Rights Issue option. </span></p>
<p><b>Step 4:</b><span style="font-weight: 400"> On the next screen you will see a list of IPOs and rights issue live. Click on &#8216;Apply&#8217; for the IPO you want to apply for.</span></p>
<p><b>Step 5:</b><span style="font-weight: 400"> The next screen will request some information from you. The number of shares you want to bid for, your bid price, date of birth. </span></p>
<p><span style="font-weight: 400">Some details like your name, PAN number, bank account number and name, branch, nationality and residential status will be pre-filled. These details cannot be altered here if need be.</span></p>
<p><span style="font-weight: 400">The information asked for under Depository details can be found in your Consolidated Account Statement (CAS) as well.</span></p>
<p><span style="font-weight: 400"><strong>Step 6</strong>: Once you proceed, you will be asked to confirm the amount to be blocked from your account, agree to necessary terms and conditions and submit the IPO application</span></p>
<h3><span id="How_to_apply_for_an_IPO_via_UPI"><strong>How to apply for an IPO via UPI</strong></span></h3>
<p><span style="font-weight: 400">The process of applying online for shares in an IPO is very simple:</span></p>
<p><span style="font-weight: 400"><strong>Step 1: </strong>Login to your trading account and select the IPO that you want to invest in.</span></p>
<p><span style="font-weight: 400"><strong>Step 2: </strong>Enter the price at which you want to apply for shares and the number of lots (</span><i><span style="font-weight: 400">explained below in the blog</span></i><span style="font-weight: 400">)</span></p>
<p><span style="font-weight: 400"><strong>Step 3: </strong>Fill the application form completely and provide your UPI ID</span></p>
<p><span style="font-weight: 400"><strong>Step 4: </strong>Approve the block funds request on the UPI app.</span></p>
<p><span style="font-weight: 400"><strong>Step 5: </strong>Done.</span></p>
<h2><span id="Accounts_Required_to_Apply_for_an_IPO_Online"><b>Accounts Required to Apply for an IPO Online</b></span></h2>
<p><span style="font-weight: 400">You need the following three accounts to invest in an IPO and trade them in the secondary market eventually:</span></p>
<ol>
<li style="font-weight: 400"><span style="font-weight: 400">Demat Account – where the shares are stored in an electronic form. It is mandatory to have a Demat account to invest in an IPO.</span></li>
<li style="font-weight: 400"><span style="font-weight: 400">Bank Account – to make payment for the applied shares. This is done via the Application Supported by Blocked Amount (ASBA) facility (</span><i><span style="font-weight: 400">explained below</span></i><span style="font-weight: 400">).</span></li>
<li style="font-weight: 400"><span style="font-weight: 400">Trading Account – this is needed to invest in an IPO online. You can open this account with a brokerage firm or a company that offers stock trading facility.</span></li>
</ol>
<h2><span id="Funding_Your_IPO_Application"><b>Funding Your IPO Application</b></span></h2>
<p><span style="font-weight: 400">The process of buying shares in an IPO is different from that in the secondary market. Regardless of a fixed price or book building issue, usually, the number of shares allotted is lower than that applied for.</span></p>
<p>Once you apply for the IPO online, regardless of the method (ASBA or UPI), the amount will get blocked for use from your bank account. It will show up in your account balance but will not be available for use/withdrawal. If your IPO application is accepted, the money will get debited after the allotment is finalised.</p>
<p>Otherwise, the amount will be released and you will be able to use the same.</p>
<p><b>Summing Up</b></p>
<p><span style="font-weight: 400">There are many things that you need to know before investing in an IPO. We hope that this article helped answer all your questions about how to purchase an IPO. Remember, while companies try to create a buzz in the markets just before launching an IPO, you need to ensure that you research the company thoroughly before investing. Also, understand the entire process well and fill the application form correctly to avoid rejection.</span></p>
<p><span style="font-weight: 400">Happy Investing!</span></p>
