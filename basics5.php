<h1 class="h2">How are Different Stocks Categorized?</h1>

<p>A stock is a share in a company. When a company is formed, there are some shareholders and investors who own the company. However, as it grows and needs funds for expansion, it has the option of ‘going public’ by issuing shares to the general public. It asks investors to buy a shareholding in the company in exchange for a proportionate share in the profits. While this is the fundamental concept of stocks, as an investor, you need to know about the different categories of stocks to make informed decisions.</p>
<p>Stocks are categorized on various criteria. Today, we will look at these criteria and understand them one by one.</p>
<div id="toc_container" class="no_bullets"><p class="toc_title">In this article</p><ul class="toc_list"><li><a href="#Categorization_Criteria_8211_Ownership">Categorization Criteria &#8211; Ownership</a></li><li><a href="#Categorization_Criteria_8211_Market_Capitalisation">Categorization Criteria &#8211; Market Capitalisation</a></li><li><a href="#Categorization_Criteria_8211_Profit_Sharing">Categorization Criteria &#8211; Profit Sharing </a></li><li><a href="#Categorization_Criteria_8211_Intrinsic_Value">Categorization Criteria &#8211; Intrinsic Value </a></li><li><a href="#Categorization_Criteria_8211_Economic_Trends">Categorization Criteria &#8211; Economic Trends </a></li><li><a href="#Categorization_Criteria_8211_Price_Volatility">Categorization Criteria &#8211; Price Volatility</a></li></ul></div>
<h2><span id="Categorization_Criteria_8211_Ownership"><strong>Categorization Criteria &#8211; Ownership</strong></span></h2>
<p>As I explained above, a stock offers ownership in a company. However, this ownership can have certain features based on the type of shares issued by the company. The primary categories based on ownership are:</p><div class="groww-content_8" id="groww-1219783760"><div class="row blog_cta">
	
		
</div></div>
<ol>
<li><strong><em>Common Stock</em></strong></a> – It offers ownership in the company with voting rights to elect the board of directors. Stockholders having common stocks are eligible to receive a part of the company’s profits via dividends. These are the most common types of stock in India.</li>
<li><strong><em>Preferred Stock</em></strong></a> – It also offers ownership in the company but doesn’t come with the same voting rights as common stocks. These stocks receive promised dividends which are not available with common stocks. Also, if the company liquidates, then these stocks get preference over common stocks.</li>
<li><strong><em>Convertible Preference Shares</em></strong> – These are initially issued as preference stocks that are converted into a fixed number of common stock at a specific time. The company can decide to offer voting rights with these stocks or not.</li>
<li><strong><em>Stocks with embedded derivative options</em></strong> – Once a company issues shares, it usually doesn’t buy them back unless it deems fit. However, some companies issue stocks with embedded derivative options – call-able or put-able. In a call-able option, the company can buy back its stocks at a specific price or a specific time. In the put-able option, the company can provide the investor with an option to sell the stock back to the company at a specific price or a specific time. These are not commonly issued by companies.</li>
</ol>
<h2><span id="Categorization_Criteria_8211_Market_Capitalisation"><strong>Categorization Criteria &#8211; Market Capitalisation</strong></span></h2>
<p>Market capitalization, in simple terms, is the total market value of a company’s outstanding shares. The calculation is simple:</p>
<p><em>Market capitalization = total number of outstanding shares x market price of one share</em></p>
<p>Let’s say that a company issues one lakh shares at Rs.10 per share and raises Rs.10 lakh. After three years, the market price of one share is Rs.30. Therefore, the market capitalization of the company will be:</p>
<p>Market Cap = 100,000 x 30 = Rs.30 lakh</p>
<p>Talking about stock market categories, shares can be classified based on their market capitalization too. There are three major categories of shares based on the market capitalization of the company:</p>
<ol>
<li>Small-cap stocks</a></li>
<li>Mid-cap stocks</a></li>
<li>Large-cap stocks</a></li>
</ol>
<p>The Securities and Exchanges Board of India (SEBI) creates a list of companies with their market capitalization and defines these companies as follows:</p>
<ol>
<li><strong><em>Large-cap companies</em></strong> – The top 100 companies in terms of market capitalization. These are the market stalwarts and famous brand names. They also tend to pay good dividends to their shareholders.</li>
<li><strong><em>Mid-cap companies</em></strong> – Those ranking between 101 and 250 in the list of companies as per market capitalization. These are growing companies that have been around for some time and with a sizable customer base. Some of these are on their way to becoming the large-caps of the future.</li>
<li><strong><em>Small-cap companies</em></strong> – All the remaining companies. The major chunk of the market consists of small-cap companies. While some of them offer a huge potential for growth, others fail to survive the economic volatility. This makes them the riskiest stocks to invest in. However, if you pick the right ones, then they also provide a great opportunity for wealth creation.</li>
</ol>
<h2><span id="Categorization_Criteria_8211_Profit_Sharing"><strong>Categorization Criteria &#8211; Profit Sharing </strong></span></h2>
<p>When you purchase a stock, you become a shareholder in the company and are eligible to receive a share of the profits based on the amount invested. Usually, companies share profits with their shareholders in the form of dividends.</p>
<p>A company can either share profits by directly distributing dividends to its shareholders or invest its profits to improve and grow its business. Based on how the company shares its profits, you get two categories of stocks:</p>
<ol>
<li><strong><em>Income Stocks</em></strong></a> – These stocks offer consistent dividend payouts. They are called income stocks since they can add to the income of the shareholder. These stocks usually belong to companies that have strong finances and can share dividends from their profits every year. However, since the profits are distributed, these companies grow at a steady pace and are considered low-risk investments.</li>
<li><strong><em>Growth Stocks</em></strong></a> – These stocks don’t pay dividends. Instead, the company reinvests its profits to grow its business. Such companies aggressively seek growth and the prices of their stocks grow rapidly. This offers the stockholder an opportunity to earn profit by selling the stocks and making capital gains. These are considered riskier than income stocks since the profits are based on the market price that can fluctuate for reasons beyond the control of the company.</li>
</ol>
<h2><span id="Categorization_Criteria_8211_Intrinsic_Value"><strong>Categorization Criteria &#8211; Intrinsic Value </strong></span></h2>
<p>While the market price of a stock depends on the demand and supply of the said stock in the market, most investors assess the financials of the company before buying its stock. For example, if a particular stock is trading at Rs.500 per share, then is the company strong enough to invest this amount? Or, is a market hype or rally driving the price up? Understanding the intrinsic value helps determine how much the market price deviates from the true value of the price of a share in the said company. Based on the intrinsic value, there are two types of stocks:</p>
<ol>
<li><strong><em>Overvalued stocks</em></strong></a> – These are stocks that have a market price that cannot be justified by its earnings outlook. Hence, the market price of such stocks is higher than their intrinsic value.</li>
<li><strong><em>Undervalued stocks</em></strong></a> – These stocks have a market price lower than their intrinsic value.</li>
</ol>
<h2><span id="Categorization_Criteria_8211_Economic_Trends"><strong>Categorization Criteria &#8211; Economic Trends </strong></span></h2>
<p>When the stock markets react to some news about the economy, all stocks don’t move in tandem. While a certain section falls with negative news about the economy, another section seems unperturbed. Based on the way stocks react to economic trends, they can be categorized into two types:</p>
<ol>
<li><strong><em>Cyclical stocks</em></strong></a> – These stocks move in sync with the economy. Hence, when the economic trends are negative, the prices of these stocks drop and vice versa. Investing in such stocks is usually beneficial in a booming economy.</li>
<li><strong><em>Defensive stocks</em></strong></a> – These stocks don’t react strongly to economic trends. Some examples of such stocks are food, medicines, insurance, etc. These are considered safer to invest in.</li>
</ol>
<h2><span id="Categorization_Criteria_8211_Price_Volatility"><strong>Categorization Criteria &#8211; Price Volatility</strong></span></h2>
<p>While some investors thrive on price volatility, others prefer stocks that are relatively stable. Based on price volatility, stocks can be classified into the following two types:</p>
<ol>
<li><strong><em>Beta stocks</em></strong></a> – Investment analysts use a statistical measure called the coefficient of beta to find the volatility in stock prices. If a stock has a higher beta, it means that the investment risk is higher.</li>
<li><strong><em>Blue-chip stocks</em></strong></a> – These are the most stable stocks since the companies are well-established. Some examples are companies like Reliance Industries, Infosys, etc.</li>
</ol>
<p><strong>Summing Up</strong></p>
<p>Understanding the different types of stocks can help you choose the right stocks to help you meet your financial goals. So, once you have an investment plan in place, use this knowledge to select stocks that are in sync with your plan. Remember, successfully investing is about managing risks efficiently while trying to optimize returns. With sufficient knowledge, you can make informed decisions and see your wealth grow. Create a category wise stock list to help you make decisions in the future.</p>
<p>Happy Investing!</p>

